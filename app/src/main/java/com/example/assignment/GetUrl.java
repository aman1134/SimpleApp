package com.example.assignment;

public class GetUrl {
    private String url ;
    private int id;
    private String title;

    public GetUrl(String url, int id, String title) {
        this.url = url;
        this.id = id;
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
